# Stdin trigger thread
# ---------------------------------------------------------------------------

class StdinTrigger(threading.Thread):
    """Background thread that pushes events onto an internal queue when the
    user hits ENTER (or types 'q'+ENTER to quit early). Using a thread keeps
    the rospy spinner / TF buffer running while we block on input()."""

    def __init__(self):
        super(StdinTrigger, self).__init__()
        self.daemon = True
        self._lock = threading.Lock()
        self._events = []   # 'snap' or 'quit'
        self._stop = threading.Event()

    def run(self):
        while not self._stop.is_set():
            try:
                line = sys.stdin.readline()
            except (KeyboardInterrupt, EOFError):
                with self._lock:
                    self._events.append("quit")
                return
            if line == "":  # EOF
                with self._lock:
                    self._events.append("quit")
                return
            line = line.strip().lower()
            with self._lock:
                if line in ("q", "quit", "exit"):
                    self._events.append("quit")
                    return
                else:
                    self._events.append("snap")

    def pop(self):
        with self._lock:
            if self._events:
                return self._events.pop(0)
            return None

    def stop(self):
        self._stop.set()


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    rospy.init_node("apriltag_precision_test", anonymous=False)

    # ---- params ---------------------------------------------------------
    frame_pattern = rospy.get_param("~frame_pattern", "tag{:d}")
    parent_frame = rospy.get_param("~parent_frame", "base_link")
    tag_ids = rospy.get_param("~tag_ids", list(range(12)))
    if not isinstance(tag_ids, (list, tuple)):
        rospy.logfatal("~tag_ids must be a list of ints, got: %r", tag_ids)
        return
    tag_ids = [int(t) for t in tag_ids]
    num_snapshots = int(rospy.get_param("~num_snapshots", 5))
    output_dir = rospy.get_param(
        "~output_dir",
        os.path.expanduser("~/.ros/apriltag_precision"))
    lookup_timeout = float(rospy.get_param("~lookup_timeout", 0.5))

    # Validate frame_pattern early
    try:
        _ = frame_pattern.format(0)
    except (IndexError, KeyError, ValueError) as e:
        rospy.logfatal("~frame_pattern is invalid: %r (%s)", frame_pattern, e)
        return

    rospy.loginfo("apriltag_precision_test starting:")
    rospy.loginfo("  parent_frame  = %s", parent_frame)
    rospy.loginfo("  frame_pattern = %s", frame_pattern)
    rospy.loginfo("  tag_ids       = %s", tag_ids)
    rospy.loginfo("  num_snapshots = %d", num_snapshots)
    rospy.loginfo("  output_dir    = %s", output_dir)

    # ---- TF -------------------------------------------------------------
    tf_buffer = tf2_ros.Buffer(cache_time=rospy.Duration(30.0))
    _ = tf2_ros.TransformListener(tf_buffer)
    rospy.sleep(0.5)  # let the listener accumulate some history

    capture = TagSnapshotCapture(
        tf_buffer, parent_frame, frame_pattern, tag_ids,
        lookup_timeout=lookup_timeout)

    # ---- ground truth ---------------------------------------------------
    gt_poses = {tid: gt_pose_in_rh_sheet_frame(tid)
                for tid in tag_ids if tid in GROUND_TRUTH_PDF}
    missing = sorted(set(tag_ids) - set(gt_poses.keys()))
    if missing:
        rospy.logwarn("No ground-truth entry for tag IDs %s -- they will be "
                      "looked up but ignored in error stats.", missing)

    # ---- interactive loop ----------------------------------------------
    stdin = StdinTrigger()
    stdin.start()

    print()
    print("Ready. Hit ENTER in this terminal to capture each snapshot.")
    print("After {} snapshots a report will be produced.".format(num_snapshots))
    print("Type 'q'+ENTER (or send EOF) to finish early.")
    print()

    snapshots = []
    pooled_rows = []
    rate = rospy.Rate(20)

    while not rospy.is_shutdown() and len(snapshots) < num_snapshots:
        ev = stdin.pop()
        if ev == "quit":
            break
        if ev == "snap":
            snap = capture.capture()
            visible = sorted(snap.keys())
            print("  snapshot {} captured: {} tags visible -> {}".format(
                len(snapshots) + 1, len(visible), visible))
            if len(visible) < 2:
                print("  (need at least 2 tags for any pair; not pooling.)")
                snapshots.append(snap)
                continue
            rows = compute_pair_errors(snap, gt_poses)
            for r in rows:
                r["snapshot"] = len(snapshots) + 1
            pooled_rows.extend(rows)
            snapshots.append(snap)
            print("  -> pooled {} pair errors so far".format(len(pooled_rows)))
        rate.sleep()

    stdin.stop()

    # ---- report ---------------------------------------------------------
    print_report(pooled_rows, len(snapshots))

    if pooled_rows:
        os.makedirs(output_dir, exist_ok=True)
        csv_path = os.path.join(output_dir, "pair_errors.csv")
        write_csv(pooled_rows, csv_path)
        write_histograms(pooled_rows, output_dir)
        print("Outputs written to: {}".format(output_dir))
    else:
        print("No pair errors collected; nothing to write.")


if __name__ == "__main__":
    try:
        main()
    except rospy.ROSInterruptException:
        pass