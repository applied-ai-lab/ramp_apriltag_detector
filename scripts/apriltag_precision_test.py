    ("zang_err_deg",       "z-axis angle deviation (signed)",   "deg",  False),
    ("zang_err_abs_deg",   "z-axis angle deviation (absolute)", "deg",  True),
    ("tx_err_mm",          "translation error tx (i-frame)",    "mm",   False),
    ("ty_err_mm",          "translation error ty (i-frame)",    "mm",   False),
    ("tz_err_mm",          "translation error tz (i-frame)",    "mm",   False),
    ("t_err_norm_mm",      "translation error norm",            "mm",   True),
    ("roll_err_deg",       "rotation error roll (ZYX)",         "deg",  False),
