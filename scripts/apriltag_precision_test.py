# ---------------------------------------------------------------------------
# Ground-truth tag layout (matches page 2 of the calibration PDF)
# ---------------------------------------------------------------------------
# Format: id -> (cx_mm, cy_mm, theta_deg) in the PDF sheet frame
# (origin top-left, +X right, +Y DOWN, theta = clockwise about Z viewed from
#  above the page, i.e. theta>0 rotates +X towards +Y_pdf).
GROUND_TRUTH_PDF = {
    0:  (27.50,  27.50,  0.0),
    1:  (85.00,  27.50, 30.0),
    2:  (142.50, 27.50, 90.0),
    3:  (27.50,  80.00, 45.0),
    4:  (85.00,  80.00,  0.0),
    5:  (142.50, 80.00, 60.0),
    6:  (27.50,  135.00, 90.0),
    7:  (85.00,  135.00, 45.0),
    8:  (142.50, 135.00,  0.0),
    9:  (27.50,  192.50,  0.0),
    10: (85.00,  192.50, 30.0),
    11: (142.50, 192.50, 45.0),
}


def gt_pose_in_rh_sheet_frame(tag_id):
    """Return the ground-truth pose of tag_id in the right-handed sheet frame
    (X right, Y up, Z out-of-page) as a 4x4 homogeneous matrix in METRES.

    Conversion from the PDF (Y-down) frame:
        x_rh =  x_pdf
        y_rh = -y_pdf
        yaw_rh = -theta_pdf   (positive yaw is CCW about +Z_rh)
    """
    cx_mm, cy_mm, theta_deg = GROUND_TRUTH_PDF[tag_id]
    x_m = cx_mm * 1e-3
    y_m = -cy_mm * 1e-3
    yaw_rad = -math.radians(theta_deg)

    T = np.eye(4)
    T[:3, :3] = R.from_euler("z", yaw_rad).as_matrix()
    T[:3, 3] = [x_m, y_m, 0.0]
    return T


# ---------------------------------------------------------------------------
# SE(3) helpers
# ---------------------------------------------------------------------------

def transform_stamped_to_matrix(ts):
    """Convert a geometry_msgs/TransformStamped into a 4x4 numpy matrix."""
    t = ts.transform.translation
    q = ts.transform.rotation
    M = np.eye(4)
    M[:3, :3] = R.from_quat([q.x, q.y, q.z, q.w]).as_matrix()
    M[:3, 3] = [t.x, t.y, t.z]
    return M


def invert_se3(T):
    """Invert a 4x4 SE(3) matrix (cheaper than np.linalg.inv)."""
    Rm = T[:3, :3]
    t = T[:3, 3]
    Tinv = np.eye(4)
    Tinv[:3, :3] = Rm.T
    Tinv[:3, 3] = -Rm.T @ t
    return Tinv


def relative_transform(T_a, T_b):
    """T_a^-1 * T_b -- pose of b expressed in a's frame."""
    return invert_se3(T_a) @ T_b


def rotation_geodesic_deg(R1, R2):
    """Geodesic angle on SO(3) between two 3x3 rotation matrices, in degrees.
    Uses arccos((trace(R1^T R2) - 1) / 2) clamped for numerical safety."""
    Rd = R1.T @ R2
    cos_t = (np.trace(Rd) - 1.0) / 2.0
    cos_t = max(min(cos_t, 1.0), -1.0)
    return math.degrees(math.acos(cos_t))


def angle_between_vectors_deg(v1, v2):
    """Angle between two 3-vectors in degrees, clamped for numerical safety."""
    n1 = np.linalg.norm(v1)
    n2 = np.linalg.norm(v2)
    if n1 < 1e-12 or n2 < 1e-12:
        return float("nan")
    c = np.dot(v1, v2) / (n1 * n2)
    c = max(min(c, 1.0), -1.0)
    return math.degrees(math.acos(c))


