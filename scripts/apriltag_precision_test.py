# Error computation
# ---------------------------------------------------------------------------

def compute_pair_errors(snapshot, gt_poses):
    """For each pair of tags visible in `snapshot`, compute error metrics
    against ground truth.

    snapshot:  dict tag_id -> 4x4 measured pose in parent frame
    gt_poses:  dict tag_id -> 4x4 ground-truth pose in sheet frame

    Returns: list of dicts, one per pair.
    """
    rows = []
    ids = sorted(snapshot.keys())
    for i, j in combinations(ids, 2):
        if i not in gt_poses or j not in gt_poses:
            continue
        T_i_meas = snapshot[i]
        T_j_meas = snapshot[j]
        T_i_gt = gt_poses[i]
        T_j_gt = gt_poses[j]

        T_ij_meas = relative_transform(T_i_meas, T_j_meas)
        T_ij_gt = relative_transform(T_i_gt, T_j_gt)

        t_meas = T_ij_meas[:3, 3]
        t_gt = T_ij_gt[:3, 3]
        R_meas = T_ij_meas[:3, :3]
        R_gt = T_ij_gt[:3, :3]

        # (a) Centre-to-centre distance error
        d_meas = np.linalg.norm(t_meas)
        d_gt = np.linalg.norm(t_gt)
        d_err_signed_mm = (d_meas - d_gt) * 1000.0
        d_err_abs_mm = abs(d_err_signed_mm)

        # (b) Out-of-plane: angle between tag-z-axes (= R[:,2] of each tag's
        # full pose, NOT the relative). This needs the raw measured z-axes
        # in the parent frame -- we compute it from T_i_meas and T_j_meas.
        z_i_meas = T_i_meas[:3, 2]
        z_j_meas = T_j_meas[:3, 2]
        z_i_gt = T_i_gt[:3, 2]
        z_j_gt = T_j_gt[:3, 2]
        zang_meas_deg = angle_between_vectors_deg(z_i_meas, z_j_meas)
        zang_gt_deg = angle_between_vectors_deg(z_i_gt, z_j_gt)
        zang_err_deg = zang_meas_deg - zang_gt_deg
        zang_err_abs_deg = abs(zang_err_deg)

        # (c) Full SE(3) relative pose error
        # Translation error in i-frame, mm per axis
        t_err_mm = (t_meas - t_gt) * 1000.0  # (dx, dy, dz)
        t_err_norm_mm = float(np.linalg.norm(t_err_mm))

        # Rotation error: R_err = R_meas * R_gt^T
        R_err = R_meas @ R_gt.T
        # Decompose to roll/pitch/yaw (intrinsic ZYX -> 'ZYX' in scipy)
        # Note: for small errors, the choice of decomposition is fine; for
        # large errors near gimbal lock the geodesic angle is the better
        # single-number summary.
        try:
            rpy_deg = R.from_matrix(R_err).as_euler("ZYX", degrees=True)
            yaw_err_deg, pitch_err_deg, roll_err_deg = rpy_deg
        except ValueError:
            roll_err_deg = pitch_err_deg = yaw_err_deg = float("nan")
        geo_err_deg = rotation_geodesic_deg(R_meas, R_gt)

        rows.append({
            "i": i, "j": j,
            "d_meas_m": d_meas, "d_gt_m": d_gt,
            "d_err_signed_mm": d_err_signed_mm,
            "d_err_abs_mm": d_err_abs_mm,
            "zang_meas_deg": zang_meas_deg,
            "zang_gt_deg": zang_gt_deg,
            "zang_err_deg": zang_err_deg,
            "zang_err_abs_deg": zang_err_abs_deg,
            "tx_err_mm": t_err_mm[0],
            "ty_err_mm": t_err_mm[1],
            "tz_err_mm": t_err_mm[2],
            "t_err_norm_mm": t_err_norm_mm,
            "roll_err_deg": roll_err_deg,
            "pitch_err_deg": pitch_err_deg,
            "yaw_err_deg": yaw_err_deg,
            "geo_err_deg": geo_err_deg,
        })
    return rows


