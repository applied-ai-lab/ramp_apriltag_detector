# Snapshot capture
# ---------------------------------------------------------------------------

class TagSnapshotCapture(object):
    def __init__(self, tf_buffer, parent_frame, frame_pattern, tag_ids,
                 lookup_timeout=0.5):
        self.tf_buffer = tf_buffer
        self.parent_frame = parent_frame
        self.frame_pattern = frame_pattern
        self.tag_ids = tag_ids
        self.lookup_timeout = rospy.Duration(lookup_timeout)

    def capture(self):
        """Return dict {tag_id: 4x4 numpy matrix} of tags currently
        available in the TF buffer. Missing/stale tags are silently
        omitted (logged at DEBUG)."""
        snapshot = {}
        now = rospy.Time(0)  # latest available
        for tid in self.tag_ids:
            child = self.frame_pattern.format(tid)
            try:
                ts = self.tf_buffer.lookup_transform(
                    self.parent_frame, child, now, self.lookup_timeout)
            except (tf2_ros.LookupException,
                    tf2_ros.ConnectivityException,
                    tf2_ros.ExtrapolationException) as e:
                rospy.logdebug("TF lookup failed for %s: %s", child, e)
                continue
            snapshot[tid] = transform_stamped_to_matrix(ts)
        return snapshot


